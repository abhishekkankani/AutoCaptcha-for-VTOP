var parse_character = function(captcha, move, error){
  for(var i = 0; i<35; i++){
    char = order[i];
    toMatch = keys[char];
    properMatch = 0.0
    totalMatch = 0.0
    height = toMatch.length
    width = toMatch[0].length

    for(var x = 0; x < height; x++){
      for(var y = 0; y < width; y++){
        if(toMatch[x][y] == 1 && y+move < 150){
          totalMatch += 1
          offset = offSet[char]
          if(captcha[x+offset][y+move] == 1)
            properMatch +=1
        }
      }
    }
    if(totalMatch!=0 && properMatch/totalMatch > error){
      // console.log(char + ": " + properMatch/totalMatch)
      return {'char': char, 'width': width };
    }
  }
  return null
}

var getCaptcha = function(captcha){
  prev_width = 0;
  move = 0;
  res = ""

  for(var i = 0; i < 6; i++){
    error = 1;
    ch = null;
    while(error > 0.84 && ch == null){
      for(var x = 0; x < 20; x++){
        if(res.length == 6){
          break;
        }
        ch = parse_character(captcha, move + x, error);
        if(ch != null){
          prev_width = ch.width;
          res = res + ch.char;
          move += prev_width + x;
          break;
        }
      }
      error -= 0.05;
    }
  }

  return res;
}


